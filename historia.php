                    	<p>La <strong>Asociación de Auxilio Póstumo del Empleado de Salud Pública</strong>, nació ante la carencia de un fondo económico y la necesidad de resolver la infinidad de cuadros dolorosos que diariamente se apreciaban con motivo del fallecimiento de los trabajadores del ramo de salud pública, ya que cada vez que se presentaba un caso se recurría a organizar colectas, las cuales consistían en hacer circular un sombrero para que según la voluntad de cada persona se depositara un aporte económico, el cual era entregado a los familiares del trabajador para ayudarlo a sufragar los gastos necesarios. Aunque en la mayor parte de las ocasiones no se podía resolver la situación económica de la familia para la compra del féretro y el panteón, se procedía únicamente a la entrega de una corona, un ramo de flores, una candela o bien unas cuantas libras de azúcar, café, pan y otros enseres necesarios para brindarle al salubrista un velorio y entierro digno.</p>	
                        <p>Esto motivó a un grupo de hombres y mujeres visionarios, a seguir el ejemplo de otras entidades mutualistas, a organizarse con el propósito de crear una institución que apoyara económicamente a las familias de todos los empleados de salud pública al momento de su deceso.</p>
                        
                        <p>Por lo que el 08 de octubre de 1,972 mediante solicitud a la Dirección General de Servicios de Salud, firmada por los trabajadores salubristas de todo el país, se promovió el nombramiento de una comisión para la realización de un estudio situacional y la elaboración del anteproyecto de estatutos, acciones que encaminarían las gestiones necesarias para la formación de la Asociación de Auxilio Póstumo del Empleado de Salud Pública.</p>	
                        

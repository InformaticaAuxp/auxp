                        <p>Durante la I Asamblea General Nacional, en 1975, Junta Directiva eligió al Primer Gerente de la Institución Prof. Víctor Manuel Valverth Fernández.
                        Ese mismo año fue emitido el Decreto 13-75 del Congreso de la República, publicado en el Diario Oficial el miércoles 02 de abril de 1975 con el cual se consolida la función de ASAPESPU como institución de servicio.

                        La primera sede fue ubicada en la 3ª. Calle 5-24 de la zona 1 de la ciudad de Guatemala inaugurándose el 14 de junio de 1975, paralelamente se realizaron las gestiones para nombrar al personal de planta administrativa, equipamiento de las instalaciones, revisión y clasificación de expedientes por departamento, organización de archivos y kardex de asociados y otras actividades administrativas.</p>
						<p>El Primer Beneficio fue cancelado a los beneficiarios del asociado: <strong>Rigoberto Mejía Peña</strong></p>
                        <p>Quien falleció el 4 de diciembre de 1974 como oficinista del Hospital Regional de Zacapa. El monto aportado por el asociado fue <strong>Q 2.45 (0.35 por 7 meses)</strong> y el monto entregado fue de <strong>Q 3,143.63</strong></p>
                        <p>El 14 de agosto de 1979 fue adquirido el terreno en el que actualmente se ubica la Asociación y gracias al aporte de todos los asociados se realizó la construcción de los primeros tres niveles del edificio durante los años 1984-1986.</p>
                        <p><strong>EVOLUCIÓN DE LA CUOTA FIJA MENSUAL Y EL BENEFICIO PÓSTUMO</strong></p>
                        
                        <p>ASAPESPU inició con un período de llamamientos y seguidamente estableció una cuota fija, la cual ha sido incrementada en cinco oportunidades y por consiguiente ha generado que el Beneficio póstumo sea mayor. Un llamamiento consistía en calcular la cuota a recaudar dependiendo del número de asociados fallecidos en un mes.

                        El período de llamamientos comprende de: Septiembre 1974 - Agosto 1991 (17 años)

                        Las cuotas aportadas oscilaron entre Q.0.35 y Q. 8.80, dependiendo de la cantidad de fallecimientos reportados durante el mes y la prestación póstuma entregada que fluctuó entre Q. 3,143.63 y Q. 14,436.80
                        </p><br>
